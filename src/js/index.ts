class Tamagotchi {

    //Properties
    public username: string
    public tamagotchiName: string
    public gender: string
    public mood: number
    public hunger: number
    public thirst: number
    public boredom: number

    constructor(UserName: string, TamagotchiName: string, Gender: string) {
        this.username = UserName
        this.tamagotchiName = TamagotchiName
        this.gender = Gender
        this.mood = 100
        this.hunger = 100
        this.thirst = 100
        this.boredom = 100
    }

    //functions

}





